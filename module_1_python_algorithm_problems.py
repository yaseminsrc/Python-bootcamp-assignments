"""**5. Restoran Hesabı: Yemek fiyatı, bahşiş oranı ve kişi sayısını alıp kişi başı tutarı hesaplayan program**"""

hesap = float(input("Toplam yemek fiyatı (TL): "))
bahsis_orani = float(input("Bahşiş oranı (%): "))
kisi_sayisi = int(input("Kişi sayısı: "))

bahsis = hesap * bahsis_orani / 100
toplam = hesap + bahsis
kisi_basi = toplam / kisi_sayisi

print("\n--- SONUÇ ---")
print(f"Bahşiş: {bahsis:.2f} TL")
print(f"Toplam hesap: {toplam:.2f} TL")
