"""**3. Yaş Gün Hesaplayıcı: Doğum tarihinden bugüne kaç gün yaşandığını hesaplayan program**"""

from datetime import datetime
doğum_tarihi = input("Doğum tarihinizi (GG/AA/YYYY) formatında giriniz: ")
gun, ay, yıl = map(int, doğum_tarihi.split("/"))

doğum = datetime(yıl, ay, gun)
bugün = datetime.now()

yaş =(bugün-doğum).days
print(f"Toplamda {yaş} gündür yaşıyorsun.")

"""**4. İndirim Hesaplayıcı: Ürün fiyatı ve indirim oranını alıp yeni fiyatı hesaplayan program**"""

